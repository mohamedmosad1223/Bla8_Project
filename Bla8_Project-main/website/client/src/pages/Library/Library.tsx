const Library: React.FC = () => {
  const { t, dir } = useLanguage();
  const [activeTab, setActiveTab ] = useState<'article' | 'book' | 'video' | 'audio'>('article');
  const [searchQuery, setSearchQuery] = useState('');
  const [selectedItem, setSelectedItem] = useState<LibraryItem | null>(null);
  const [audioDurations, setAudioDurations] = useState<Record<string, string>>({});
  const [videoDurations, setVideoDurations] = useState<Record<string, string>>({});
  const [pdfPages, setPdfPages] = useState<Record<string, string>>({});

  const formatAudioDuration = (seconds: number): string => {
    if (!Number.isFinite(seconds) || seconds < 0) return t('library.notAvailable');
    const total = Math.floor(seconds);
    const hrs = Math.floor(total / 3600);
    const mins = Math.floor((total % 3600) / 60);
    const secs = total % 60;
    if (hrs > 0) {
      return `${String(hrs).padStart(2, '0')}:${String(mins).padStart(2, '0')}:${String(secs).padStart(2, '0')}`;
    }
    return `${String(mins).padStart(2, '0')}:${String(secs).padStart(2, '0')}`;
  };

  useEffect(() => {
    const audioItems = mockData.filter((item) => item.type === 'audio' && item.url);
    if (audioItems.length === 0) return;

    let cancelled = false;
    const cleanupFns: Array<() => void> = [];

    audioItems.forEach((item) => {
      const audio = new Audio();
      audio.preload = 'metadata';

      const handleLoadedMetadata = () => {
        if (cancelled) return;
        const realDuration = formatAudioDuration(audio.duration);
        setAudioDurations((prev) => ({ ...prev, [item.id]: realDuration }));
      };

      const handleError = () => {
        if (cancelled) return;
        setAudioDurations((prev) => ({ ...prev, [item.id]: t('library.notAvailable') }));
      };

      audio.addEventListener('loadedmetadata', handleLoadedMetadata);
      audio.addEventListener('error', handleError);
      audio.src = item.url;

      cleanupFns.push(() => {
        audio.removeEventListener('loadedmetadata', handleLoadedMetadata);
        audio.removeEventListener('error', handleError);
        audio.src = '';
      });
    });

    return () => {
      cancelled = true;
      cleanupFns.forEach((fn) => fn());
    };
  }, []);

  useEffect(() => {
    const videoItems = mockData.filter((item) => item.type === 'video' && item.url);
    if (videoItems.length === 0) return;

    let cancelled = false;
    const cleanupFns: Array<() => void> = [];

    videoItems.forEach((item) => {
      const video = document.createElement('video');
      video.preload = 'metadata';

      const handleLoadedMetadata = () => {
        if (cancelled) return;
        const realDuration = formatAudioDuration(video.duration);
        setVideoDurations((prev) => ({ ...prev, [item.id]: realDuration }));
      };

      const handleError = () => {
        if (cancelled) return;
        setVideoDurations((prev) => ({ ...prev, [item.id]: t('library.notAvailable') }));
      };

      video.addEventListener('loadedmetadata', handleLoadedMetadata);
      video.addEventListener('error', handleError);
      video.src = item.url;

      cleanupFns.push(() => {
        video.removeEventListener('loadedmetadata', handleLoadedMetadata);
        video.removeEventListener('error', handleError);
        video.src = '';
      });
    });

    return () => {
      cancelled = true;
      cleanupFns.forEach((fn) => fn());
    };
  }, []);

  useEffect(() => {
    const pdfItems = mockData.filter((item) => item.type === 'book' && item.url);
    if (pdfItems.length === 0) return;

    let cancelled = false;

    const loadAllPdfPages = async () => {
      try {
        const pdfjs = await import('pdfjs-dist/build/pdf.mjs');
        pdfjs.GlobalWorkerOptions.workerSrc = new URL(
          'pdfjs-dist/build/pdf.worker.min.mjs',
          import.meta.url,
        ).toString();

        await Promise.all(
          pdfItems.map(async (item) => {
            try {
              const loadingTask = pdfjs.getDocument(item.url);
              const pdfDoc = await loadingTask.promise;
              if (cancelled) return;
              setPdfPages((prev) => ({ ...prev, [item.id]: String(pdfDoc.numPages) }));
            } catch {
              if (cancelled) return;
              setPdfPages((prev) => ({ ...prev, [item.id]: t('library.notAvailable') }));
            }
          }),
        );
      } catch {
        if (!cancelled) {
          pdfItems.forEach((item) => {
            setPdfPages((prev) => ({ ...prev, [item.id]: t('library.notAvailable') }));
          });
        }
      }
    };

    loadAllPdfPages();

    return () => {
      cancelled = true;
    };
  }, []);

  const dataWithRealAudioDurations = useMemo(() => (
    mockData.map((item) => (
      item.type === 'audio'
        ? { ...item, duration: audioDurations[item.id] ?? item.duration ?? t('library.loading') }
        : item.type === 'video'
          ? { ...item, duration: videoDurations[item.id] ?? item.duration ?? t('library.loading') }
          : item.type === 'book'
            ? { ...item, pages: pdfPages[item.id] ?? item.pages ?? t('library.loading') }
            : item
    ))
  ), [audioDurations, pdfPages, videoDurations, t]);

  const handleItemClick = (item: LibraryItem) => {
    // Basic mobile detection
    const isMobile = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
    
    if (isMobile && item.type === 'book' && item.url) {
      window.open(item.url, '_blank');
    } else {
      setSelectedItem(item);
    }
  };

  const filteredData = dataWithRealAudioDurations.filter(item =>
    item.type === activeTab &&
    (item.title.includes(searchQuery) || item.author.includes(searchQuery))
  );

  return (
    <div className="library-container" dir={dir}>
      {/* Tabs at the very top */}
      <div className="library-tabs">
        <button 
          className={`tab-item ${activeTab === 'article' ? 'active' : ''}`}
          onClick={() => setActiveTab('article')}
        >
          <FileText size={20} />
          <span>{t('library.articles') || 'المقالات'}</span>
        </button>
        <button 
          className={`tab-item ${activeTab === 'book' ? 'active' : ''}`}
          onClick={() => setActiveTab('book')}
        >
          <Book size={20} />
          <span>{t('library.books') || 'الكتب'}</span>
        </button>
        <button 
          className={`tab-item ${activeTab === 'video' ? 'active' : ''}`}
          onClick={() => setActiveTab('video')}
        >
          <Video size={20} />
          <span>{t('library.videos') || 'الفيديوهات'}</span>
        </button>
        <button 
          className={`tab-item ${activeTab === 'audio' ? 'active' : ''}`}
          onClick={() => setActiveTab('audio')}
        >
          <Mic size={20} />
          <span>{t('library.audios') || 'الصوتيات'}</span>
        </button>
      </div>

      <div className="library-search-header">
        <div className="search-input-inner">
          <Search size={20} className="search-icon" />
          <input 
            type="text" 
            placeholder={activeTab === 'book' ? (t('library.searchBook') || "ابحث عن كتاب") : activeTab === 'video' ? (t('library.searchVideo') || "ابحث عن فيديو") : activeTab === 'audio' ? (t('library.searchAudio') || "ابحث عن صوتيات") : (t('library.searchArticle') || "ابحث عن مقالة")} 
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
          />
        </div>
      </div>

      {/* Content Grid (or List for Audio) */}
      {activeTab === 'audio' ? (
        <div className="audio-list">
          {filteredData.map(item => (
            <div 
              key={item.id} 
              className="audio-list-item" 
              onClick={() => handleItemClick(item)}
              style={{ cursor: 'pointer' }}
            >
              <div className="audio-info">
                <span className="audio-title">{item.title}</span>
                <span className="audio-author">{item.author}</span>
              </div>
              <div className="audio-actions">
                <div className="audio-meta">
                  <span className="audio-duration"> {item.duration ?? t('library.notAvailable')}</span>
                </div>
                <button className="audio-play-btn">
                  <Play size={20} fill="currentColor" />
                </button>
              </div>
            </div>
          ))}
        </div>
      ) : (
        <div className="library-grid">
          {filteredData.map(item => (
            <div 
              key={item.id} 
              className="library-card" 
              onClick={() => handleItemClick(item)}
              style={{ cursor: 'pointer' }}
            >
              <div className="card-image-wrap">
                <img src={item.imageUrl} alt={item.title} />
                {item.type === 'video' && (
                  <div className="video-play-overlay">
                    <Play fill="currentColor" size={24} />
                  </div>
                )}
              </div>
              <div className="card-content">
                {item.type === 'article' && (
                  <>
                    <span className="card-category">
                      <Book size={14} /> {t('library.articles') || 'المقالات'}
                    </span>
                    <h3 className="card-title">{item.title}</h3>
                    <div className="card-author-row">
                      <div className="author-avatar">
                        <img src={item.authorImageUrl!} alt={item.author} />
                      </div>
                      <span className="card-author-name">{item.author}</span>
                    </div>
                  </>
                )}

                {(item.type === 'book' || item.type === 'video') && (
                  <>
                     <span className="card-author-name text-center text-gray" style={{ textAlign: 'center', color: '#718096', fontSize: '0.85rem' }}>{item.author}</span>
                     <h3 className="card-title" style={{ textAlign: 'center' }}>{item.title}</h3>
                     <div className="card-stats-footer" style={{ justifyContent: 'center', gap: '16px' }}>
                        <div className="stat-item">
                          <span>{item.type === 'book' ? `${t('library.pagesCount') || 'عدد الصفحات:'} ${item.pages}` : `${t('library.duration') || 'المدة:'} ${item.duration}`}</span>
                        </div>
                     </div>
                  </>
                )}
              </div>
            </div>
          ))}
        </div>
      )}

      {/* Viewer Modal */}
      {selectedItem && (
        <div className="library-viewer-modal" onClick={() => setSelectedItem(null)}>
          <div className="library-viewer-content" onClick={e => e.stopPropagation()}>
            <button className="library-viewer-close" onClick={() => setSelectedItem(null)}>
               <X size={24} color="#374151" />
            </button>
            <h2 className="library-viewer-title">{selectedItem.title}</h2>
            <div className="library-viewer-body">
              {selectedItem.type === 'video' && (
                <video controls preload="metadata" className="library-viewer-video" poster={selectedItem.imageUrl}>
                  <source src={selectedItem.url} type="video/mp4" />
                  Your browser does not support the video tag.
                </video>
              )}
              {selectedItem.type === 'book' && (
                <iframe src={selectedItem.url} className="library-viewer-pdf" title={selectedItem.title} />
              )}
              {selectedItem.type === 'article' && (
                <div className="library-viewer-article-wrapper">
                  <div className="library-viewer-article-content">
                    {selectedItem.content}
                  </div>
                </div>
              )}
              {selectedItem.type === 'audio' && (
                <div className="library-viewer-audio-wrapper">
                  <div className="library-viewer-audio-cover-wrap">
                    <img src={selectedItem.imageUrl || '/library/غلاف.jpg'} alt={selectedItem.title} />
                  </div>
                  <h3 className="library-viewer-audio-author">{selectedItem.author}</h3>
                  <audio controls className="library-viewer-audio-player" autoPlay>
                    <source src={selectedItem.url} type="audio/mpeg" />
                    Your browser does not support the audio element.
                  </audio>
                </div>
              )}
              {(selectedItem.type !== 'video' && selectedItem.type !== 'book' && selectedItem.type !== 'article' && selectedItem.type !== 'audio') && (
                <div className="library-viewer-empty">{t('library.contentNotAvailable') || 'هذا المحتوى غير متوفر للعرض المباشر'}</div>
              )}
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

export default Library;
